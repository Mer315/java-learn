package sumedha_practice;
import java.util.*;
import java.util.Scanner;

public class Concat {
	
	public static void main(String args[]) {
		String first;
		String second;
		String out;
		Scanner s = new Scanner(System.in);
		System.out.println("enter first:");
		first = s.nextLine();
		System.out.println("enter second");
		second = s.nextLine();
		out = first + second;
		System.out.println(out);
		
		
	}
	
}
